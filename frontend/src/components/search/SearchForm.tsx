export default function SearchForm({ onSearch }: Props) {
  const [tipos, setTipos] = useState(TIPOS_HARDCODEADOS);
  const [dependencias, setDependencias] = useState(DEPENDENCIAS_HARDCODEADAS);
  const [openDep, setOpenDep] = useState(false);

  const [form, setForm] = useState({
    tipo: '',
    numero: '',
    texto: '',
    dependencia: '',
    dateFrom: undefined as Date | undefined,
    dateTo: undefined as Date | undefined,
    limit: 10,
  });

  useEffect(() => {
    fetch('/api/infoleg/tipos')
      .then(r => r.json())
      .then(fetched => {
        if (JSON.stringify(fetched) !== JSON.stringify(TIPOS_HARDCODEADOS)) {
          setTipos(fetched);
        }
      })
      .catch(console.error);

    fetch('/api/infoleg/dependencies')
      .then(r => r.json())
      .then(fetched => {
        if (
          JSON.stringify(fetched) !== JSON.stringify(DEPENDENCIAS_HARDCODEADAS)
        ) {
          setDependencias(fetched);
        }
      })
      .catch(console.error);
  }, []);

  // ---------------------------------------------
  // handlers
  // ---------------------------------------------
  const updateField = (name: string, value: string) =>
    setForm(f => ({ ...f, [name]: value }));

  function handleSubmit(e: React.FormEvent) {
    e.preventDefault();
    if (!form.tipo) return;

    const params: Record<string, unknown> = {
      tipo: form.tipo,
      numero: form.numero ? Number(form.numero) : undefined,
      texto: form.texto || undefined,
      dependencia: form.dependencia || undefined,
      publicacion_desde: form.dateFrom
        ? format(form.dateFrom, 'yyyy-MM-dd')
        : undefined,
      publicacion_hasta: form.dateTo
        ? format(form.dateTo, 'yyyy-MM-dd')
        : undefined,
      limit: form.limit,
      offset: 1,
    };
    onSearch(params);
  }

  const handleReset = () =>
    setForm({
      tipo: '',
      numero: '',
      texto: '',
      dependencia: '',
      dateFrom: undefined,
      dateTo: undefined,
      limit: 10,
    });

  const textoRef = useRef<HTMLInputElement>(null);

  const insertAtCursor = (text: string) => {
    const input = textoRef.current;
    if (!input) return;

    const start = input.selectionStart ?? 0;
    const end = input.selectionEnd ?? 0;
    const before = form.texto.slice(0, start);
    const after = form.texto.slice(end);

    const newTexto = `${before}${text}${after}`;
    setForm(f => ({ ...f, texto: newTexto }));

    // Move cursor after inserted text
    setTimeout(() => {
      input.selectionStart = input.selectionEnd = start + text.length;
      input.focus();
    }, 0);
  };

  return (
    <div className='sticky top-6'>
      <div className='bg-card rounded-2xl border p-6 shadow-sm'>
        <div className='mb-4 flex items-center justify-between'>
          <h2 className='text-lg font-semibold'>Buscar Legislación</h2>
          <InfoSearch />
        </div>

        <form onSubmit={handleSubmit} className='space-y-4'>
          {/* Tipo de Norma */}
          <div className='space-y-1'>
            <label className='text-sm font-medium'>Tipo de Norma *</label>
            <Select
              value={form.tipo}
              onValueChange={val => updateField('tipo', val)}
              required
            >
              <SelectTrigger className='w-full'>
                <SelectValue placeholder='Seleccionar' />
              </SelectTrigger>
              <SelectContent>
                {tipos.map(t => (
                  <SelectItem key={t.route} value={t.route}>
                    {t.detalle}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
          </div>

          {/* Número */}
          <Input
            type='number'
            name='numero'
            placeholder='Número'
            value={form.numero}
            onChange={e => {
              const val = e.target.value;
              if (val === '' || Number(val) >= 0) {
                updateField('numero', val);
              }
            }}
            className='appearance-none'
            min='0'
          />

          {/* Texto */}
          <div className='space-y-1'>
            <label className='text-sm font-medium' htmlFor='texto'>
              Texto
            </label>
            <Input
              id='texto'
              name='texto'
              ref={textoRef}
              placeholder='Ej: "educación AND salud"'
              value={form.texto}
              onChange={e => updateField('texto', e.target.value)}
            />

            {/* Operator buttons */}
            <div className='flex gap-2 pt-1'>
              <Button
                type='button'
                size='sm'
                variant='outline'
                onClick={() => insertAtCursor(' AND ')}
              >
                + AND
              </Button>
              <Button
                type='button'
                size='sm'
                variant='outline'
                onClick={() => insertAtCursor(' OR ')}
              >
                + OR
              </Button>
              <Button
                type='button'
                size='sm'
                variant='outline'
                onClick={() => insertAtCursor(' NOT ')}
              >
                + NOT
              </Button>
            </div>

            <p className='text-xs text-muted-foreground'>
              Usá operadores lógicos para mejorar la búsqueda.
            </p>
          </div>

          {/* Dependencia (con Combobox) */}
          <div className='space-y-1'>
            <label className='text-sm font-medium'>Dependencia</label>
            <Popover open={openDep} onOpenChange={setOpenDep}>
              <PopoverTrigger asChild>
                <Button
                  variant='outline'
                  role='combobox'
                  aria-expanded={openDep}
                  className='w-full justify-between truncate'
                >
                  <span
                    className={cn(
                      'truncate max-w-full',
                      !form.dependencia && 'text-muted-foreground font-normal',
                    )}
                    title={form.dependencia || 'Seleccionar dependencia'}
                  >
                    {form.dependencia || 'Seleccionar dependencia'}
                  </span>
                  <ChevronsUpDown className='ml-2 h-4 w-4 shrink-0 opacity-50' />
                </Button>
              </PopoverTrigger>

              <PopoverContent className='min-w-[var(--radix-popover-trigger-width)] p-0 max-h-64 overflow-y-auto'>
                <Command>
                  <CommandInput
                    placeholder='Buscar dependencia...'
                    className='h-9'
                  />

                  <CommandList>
                    <CommandEmpty>No se encontraron dependencias.</CommandEmpty>
                    <CommandGroup>
                      {dependencias.map(dep => (
                        <CommandItem
                          key={dep}
                          value={dep}
                          onSelect={currentValue => {
                            updateField(
                              'dependencia',
                              form.dependencia === currentValue
                                ? ''
                                : currentValue,
                            );
                            setOpenDep(false);
                          }}
                        >
                          {dep}
                          <Check
                            className={cn(
                              'ml-auto h-4 w-4',
                              form.dependencia === dep
                                ? 'opacity-100'
                                : 'opacity-0',
                            )}
                          />
                        </CommandItem>
                      ))}
                    </CommandGroup>
                  </CommandList>
                </Command>
              </PopoverContent>
            </Popover>
          </div>

          {/* Rango de Fechas */}
          <label className='text-sm font-medium'>Fechas de Publicación</label>
          <div className='flex flex-col gap-2 md:flex-row'>
            <div className='w-full md:w-1/2 min-w-0'>
              <DateSelect
                value={form.dateFrom}
                onChange={date => setForm(f => ({ ...f, dateFrom: date }))}
                yearFrom={1810}
                yearTo={new Date().getFullYear()}
                placeholder='Desde'
              />
            </div>
            <div className='w-full md:w-1/2'>
              <DateSelect
                value={form.dateTo}
                onChange={date => setForm(f => ({ ...f, dateTo: date }))}
                yearFrom={1810}
                yearTo={new Date().getFullYear()}
                placeholder='Hasta'
              />
            </div>
          </div>

          {/* Límite */}
          <div className='space-y-1'>
            <label className='text-sm font-medium'>Resultados por página</label>
            <Select
              value={String(form.limit)}
              onValueChange={val => updateField('limit', val)}
            >
              <SelectTrigger className='w-full'>
                <SelectValue />
              </SelectTrigger>
              <SelectContent>
                {[5, 10, 20, 50].map(n => (
                  <SelectItem key={n} value={String(n)}>
                    {n}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
          </div>

          {/* Botones */}
          <div className='flex gap-2'>
            <Button type='button' variant='outline' onClick={handleReset}>
              Limpiar
            </Button>
            <Button type='submit' className='flex-1'>
              <SearchIcon className='mr-2 h-4 w-4' /> Buscar
            </Button>
          </div>
        </form>
      </div>
    </div>
  );
}
